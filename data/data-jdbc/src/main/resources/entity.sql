CREATE TABLE ENTITY
(
    ID          BIGINT         NOT NULL AUTO_INCREMENT,
    NAME        VARCHAR(16)    NOT NULL,
    GENDER      TINYINT        NOT NULL,
    MONEY       DECIMAL(10, 2) NOT NULL,
    CREATE_TIME datetime       NOT NULL,
    IS_DELETE   TINYINT        NOT NULL,
    CONSTRAINT ENTITY_PK PRIMARY KEY (ID)
);